public class Product {

    int id;
    String name;
    double priceWhenBuy;
    double priceWhenSell;

    public Product(int id, String name, double priceWhenBuy, double priceWhenSell) {
        this.id = id;
        this.name = name;
        this.priceWhenBuy = priceWhenBuy;
        this.priceWhenSell = priceWhenSell;
    }
}
